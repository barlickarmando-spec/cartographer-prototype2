export interface StateQoLRawData {
  // FIPS and identifiers
  state_fips: string;
  state_postal: string;

  // Safety (lower is better for all)
  violent_rate_per_100k: number;      // FBI UCR violent crime rate per 100k
  property_rate_per_100k: number;     // FBI UCR property crime rate per 100k
  pit_rate_per_10k: number;           // Point-in-time homelessness rate per 10k residents

  // Healthcare
  life_expectancy: number;            // Life expectancy at birth (years)
  uninsured_pct: number;              // % uninsured

  // Infrastructure
  broadband_pct: number;              // % households with broadband
  saidi_minutes: number;              // System Average Interruption Duration Index
  saifi_interruptions: number;        // System Average Interruption Frequency Index
  disaster_eal_millions: number;      // Expected Annual Loss from disasters ($M)

  // Environment
  aqi_median: number;                 // Median Air Quality Index (lower is better)
  water_violation_rate: number;       // Drinking water violations per 1000 people served (lower is better)
  waste_compliance_score: number;     // 0-100, higher is better

  // Education
  naep_math_g8: number;              // NAEP grade 8 math scale score (typically 260-300)
  naep_reading_g8: number;           // NAEP grade 8 reading scale score (typically 250-280)
  naep_science_g8: number;           // NAEP grade 8 science scale score (typically 140-165)
  graduation_rate: number;           // ACGR % (typically 80-95)
  avg_public_tuition_instate: number; // Average public in-state tuition+fees ($)
  preschool_enrollment_pct: number;  // % of 3-4 year olds enrolled
  pupil_teacher_ratio: number;       // Students per teacher
  childcare_infant_annual: number;   // Annual infant center-based childcare cost ($)

  // Cost/Price level
  rpp_all_items: number;             // Regional Price Parity (national=100)
}

